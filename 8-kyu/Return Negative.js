///Check if the given number is positive using an if statement.
///If it's positive, multiply it by -1 to make it negative.
///If it's already negative, leave it as is.



function makeNegative(num) {
    if (num > 0) {
      return -1 * num;
    } else {
      return num;
    }
  }

